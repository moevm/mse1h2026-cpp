            "- Следит за знаком: если знаменатель отрицательный, минус переносится в числитель.\n\n"
            "3) Конструктор\n"
            "- Принимает числитель и знаменатель.\n"
            "- Если знаменатель равен 0, выбрасывает исключение std::invalid_argument.\n\n"
            "4) Оператор * (умножение)\n"
            "- Перегрузка умножения одной дроби на другую.\n"
            "- Возвращает новый объект Fraction.\n\n"
            "5) Оператор + (сложение)\n"
            "- Перегрузка сложения двух дробей.\n"
            "- Находит общий знаменатель, складывает и возвращает новый результат (автоматически сокращенный благодаря simplify).\n\n"
            "6) Оператор << (вывод)\n"
            "- Дружественная функция для вывода в поток в формате num/denom."
